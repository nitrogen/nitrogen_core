-module (element_placeholder).
-include("wf.hrl").
-export([
    reflect/0,
    render_element/1
]).

-spec reflect() -> [atom()].
reflect() -> record_info(fields, placeholder).

-spec render_element(#placeholder{}) -> body().
render_element(Record) -> 
    Record#placeholder.body.
